import { cookies } from "next/headers";
import TanStackProvider from "@/components/TanStackProvider/TanStackProvider";
import Sidebar from "@/components/Sidebar/Sidebar";
import "./globals.css";
import { Lato, Comfortaa } from "next/font/google";

const fontsLato = Lato({
  variable: "--font-lato",
  subsets: ["latin"],
  weight: ["100", "300", "400", "700", "900"],
  display: "swap",
});

const fontsComfortaa = Comfortaa({
  variable: "--font-comfortaa",
  subsets: ["latin"],
  weight: ["300", "400", "500", "600", "700"],
  display: "swap",
});

export const metadata = {
  title: "Лелека",
  description: "Generated by create next app",
};

interface RootLayoutProps {
  children: React.ReactNode;
}

// Асинхронна функція для перевірки автентифікації на сервері
async function getAuthStatus() {
  const cookieStore = await cookies(); // <--- Виправлення тут
  const token = cookieStore.get("auth_token");
  return !!token;
}

export default async function RootLayout({ children }: RootLayoutProps) {
  const isAuthenticated = await getAuthStatus();

  return (
    <html lang="en">
      <body className={`${fontsLato.variable} ${fontsComfortaa.variable}`}>
        <TanStackProvider>
          <Sidebar initialAuthStatus={isAuthenticated} />
          <main>{children}</main>
        </TanStackProvider>
      </body>
    </html>
  );
}
